package collections;
//1: Print all the numbers between -15 to 10.

public class PrintMinus15to20 {

	public static void main(String[] args) {
		int length = 20;
		int len = -15;
		for (int j = len; j < 0; j++) {
			System.out.println(j);
		}
		for (int i = 0; i <= length; i++) {
			System.out.println(i);
		}

	}

}
